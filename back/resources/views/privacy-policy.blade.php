        <h1>Política de Privacidad</h1>
        <p>
            En CustomAcademy, valoramos tu privacidad y nos comprometemos a proteger tu información personal. Esta política de privacidad explica cómo recopilamos, utilizamos, y compartimos tu información cuando utilizas nuestro sitio web y servicios.
        </p>

        <h2>1. Información que recopilamos</h2>
        <p>
            Recopilamos información de diversas maneras cuando utilizas nuestro sitio web y servicios:
        </p>
        <ul>
            <li><strong>Información proporcionada por el usuario:</strong> Recopilamos la información que nos proporcionas directamente, como cuando creas una cuenta, actualizas tu perfil, te inscribes en un curso, o te pones en contacto con nosotros. Esta información puede incluir tu nombre, dirección de correo electrónico, número de teléfono, foto de perfil, y cualquier otra información que decidas proporcionar.</li>
            <li><strong>Información sobre el uso:</strong> Recopilamos información sobre tu actividad en nuestro sitio, como los cursos en los que te inscribes, tu progreso en esos cursos, las evaluaciones y comentarios que realizas, y las interacciones con otros usuarios.</li>
            <li><strong>Información técnica:</strong> Recopilamos información técnica automáticamente cuando utilizas nuestro sitio web, como tu dirección IP, tipo de navegador, sistema operativo, páginas visitadas, y el tiempo que pasas en nuestro sitio.</li>
        </ul>

        <h2>2. Cómo utilizamos tu información</h2>
        <p>
            Utilizamos la información que recopilamos para:
        </p>
        <ul>
            <li>Proporcionar, mantener y mejorar nuestros servicios.</li>
            <li>Personalizar tu experiencia en CustomAcademy, por ejemplo, recomendando cursos que puedan interesarte.</li>
            <li>Comunicarnos contigo sobre tu cuenta, los cursos en los que estás inscrito, actualizaciones importantes y ofertas promocionales.</li>
            <li>Monitorear y analizar tendencias, uso y actividades en relación con nuestros servicios para mejorar nuestros servicios y la experiencia del usuario.</li>
            <li>Proteger la seguridad y la integridad de nuestro sitio web y servicios.</li>
        </ul>

        <h2>3. Cómo compartimos tu información</h2>
        <p>
            No compartimos tu información personal con terceros, excepto en las siguientes circunstancias:
        </p>
        <ul>
            <li><strong>Proveedores de servicios:</strong> Podemos compartir tu información con proveedores de servicios que realizan funciones en nuestro nombre, como procesamiento de pagos, análisis de datos, servicios de marketing, y soporte al cliente.</li>
            <li><strong>Por razones legales:</strong> Podemos divulgar tu información si así lo exige la ley o si creemos de buena fe que dicha acción es necesaria para cumplir con una obligación legal, proteger nuestros derechos, propiedad o seguridad, o la de nuestros usuarios y el público.</li>
        </ul>

        <h2>4. Seguridad de tu información</h2>
        <p>
            Tomamos medidas razonables para proteger la información que recopilamos de pérdida, robo, uso indebido y acceso no autorizado, divulgación, alteración y destrucción. Sin embargo, ningún método de transmisión por Internet o método de almacenamiento electrónico es 100% seguro. Por lo tanto, no podemos garantizar su seguridad absoluta.
        </p>

        <h2>5. Tus derechos y opciones</h2>
        <p>
            Tienes ciertos derechos en relación con tu información personal, incluyendo el derecho a acceder, corregir o eliminar la información que tenemos sobre ti. También puedes optar por no recibir comunicaciones promocionales de nuestra parte siguiendo las instrucciones en esas comunicaciones.
        </p>

        <h2>6. Cambios a esta política de privacidad</h2>
        <p>
            Podemos actualizar esta política de privacidad de vez en cuando para reflejar cambios en nuestras prácticas o en la ley aplicable. Te notificaremos sobre cualquier cambio material mediante la publicación de la nueva política de privacidad en nuestro sitio web. Te recomendamos que revises esta política periódicamente para estar al tanto de cualquier cambio.
